public class PromocaoAutomatica {
    public static void main(String[] args) {
        byte a = 10;
        short b = 20;
        char c = 'A';
        int d = 30;
        long e = 40L;
        float f = 1.5f;
        double g = 2.5;

        int resultado = a + b;
        long resultado2 = d + e;
        double resultado3 = f + g;

        System.out.println("Valor de a + b: " + resultado);
        System.out.println("Valor do char: " + c);
        System.out.println("Valor de d + e: " + resultado2);
        System.out.println("Valor de f + g: " + resultado3);






    }
}